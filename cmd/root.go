// rootCmd represents the base command when called without any subcommands
var rootCmd = &cobra.Command{
	Use:   "godo",
	Short: "A todo list management CLI tool",
	Long: `godo is a command-line todo list manager that helps you track and organize tasks.

Features:
- Add, complete, and remove todo items
- List all outstanding tasks
- Mark tasks as done
- Organize tasks by priority
- Save tasks persistently
- Simple command-line interface

Use 'godo help' to see available commands.`,
	// Uncomment the following line if your bare application
	// has an action associated with it:
	// Run: func(cmd *cobra.Command, args []string) { },
}

// Execute adds all child commands to the root command and sets flags appropriately.
// This is called by main.main(). It only needs to happen once to the rootCmd.
func Execute() {
	// Set list command as default
	if len(os.Args) == 1 {
		os.Args = append(os.Args, "list")
	}

	err := rootCmd.Execute()
	if err != nil {
		os.Exit(1)
	}
}

